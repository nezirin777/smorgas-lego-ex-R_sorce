    /**
     * 板URLから板名を取得します。
     * @param {string} boardUrl 板URL
     * @returns {string} 板名
     */
    getName(boardUrl) {
        if (!boardUrl) return "(不明)";

        const re = /^(?:https?:\/\/)?[^\.]+\.(.*)/;
        const searchKey = boardUrl.replace(re, "$1").replace(/^[25]ch\.(.*)/, "5ch.$1");

        for (const key in this.nameTbl) {
            if (Object.prototype.hasOwnProperty.call(this.nameTbl, key)) {
                const normalizedKey = key.replace(re, "$1");
                if (searchKey === normalizedKey) {
                    return this.nameTbl[key];
                }
            }
        }
        return "(不明)";
    }

    /**
     * スレッドURLから板URLに変換します。
     * @param {string} url スレッドURL
     * @returns {string} 板URL
     */
    getUrlByThread(url) {
        if (!url) return "";

        if (url.indexOf('test/read.cgi') !== -1) {
            // 5chタイプのスレッド
            return url.replace(/^(https?:\/\/[^\/]+)\/test\/read\.cgi\/([^\/]+)\/.*/, "$1/$2/");
        } else if (url.indexOf('bbs/read.cgi') !== -1) {
            if (url.indexOf('jbbs.') !== -1) {
                // したらば掲示板のスレッド(最初のドットまでは削除)
                return url.replace(/^(https?:\/\/[^\/]+)\/bbs\/read\.cgi\/([^\/]+)\/([^\/]+)\/.*/, "$1/$2/$3/");
            } else {
                // その他
                return url.replace(/^(https?:\/\/[^\/]+)\/bbs\/read\.cgi\/([^\/]+)\/.*/, "$1/$2/");
            }
        } else {
            console.log(`lego-ex-R:[ERROR]:Board:getUrlByThread(${url}) Not thread!`);
            return url;
        }
    }
