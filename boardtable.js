/**
 * 従来互換のフラットな板名テーブルです。
 * @type {Object}
 */
nameTbl: {},

/**
 * ドメイン別定義から従来互換の nameTbl を構築します。
 * @return {Object}
 */
buildNameTbl(){
	const flat = {};
	for(const hosts of Object.values(this.domainTables)){
		for(const [host, boards] of Object.entries(hosts)){
			for(const [boardKey, boardName] of Object.entries(boards)){
				flat[boardKey ? `${host}/${boardKey}/` : `${host}/`] = boardName;
			}
		}
	}
	this.nameTbl = flat;
	return flat;
},

/**
 * ホスト名を内部参照用に正規化します。
 * @param {string} host ホスト名
 * @return {string}
 */
normalizeHost(host){
	const normalizedHost = String(host || "").toLowerCase();
	if(!normalizedHost) return "";
	if(/^(?:\d{1,3}\.){3}\d{1,3}$/.test(normalizedHost) || normalizedHost === "localhost") return normalizedHost;
	if(normalizedHost.endsWith(".5ch.net")) return normalizedHost.replace(/\.5ch\.net$/, ".5ch.io");
	if(normalizedHost === "5ch.net") return "5ch.io";
	if(normalizedHost.endsWith(".2ch.net")) return normalizedHost.replace(/\.2ch\.net$/, ".5ch.io");
	if(normalizedHost === "2ch.net") return "5ch.io";
	return normalizedHost;
},

/**
 * ホスト名からドメイングループ名を取得します。
 * @param {string} host ホスト名
 * @return {string}
 */
getDomainGroup(host){
	const normalizedHost = this.normalizeHost(host);
	if(!normalizedHost) return "";
	if(/^(?:\d{1,3}\.){3}\d{1,3}$/.test(normalizedHost) || normalizedHost === "localhost") return normalizedHost;
	const parts = normalizedHost.split(".");
	return parts.length >= 2 ? parts.slice(-2).join(".") : normalizedHost;
},

/**
 * 板URLからホスト名と板キーを抽出します。
 * @param {string} boardUrl 板URL
 * @return {{host: string, boardKey: string}|null}
 */
parseBoardUrl(boardUrl){
	const match = String(boardUrl || "").match(/^(?:https?:\/\/)?([^\/?#]+)(?:\/([^?#]*))?/i);
	if(!match) return null;
	const host = this.normalizeHost(match[1]);
	const boardKey = String(match[2] || "").replace(/^\/+|\/+$/g, "");
	return { host, boardKey };
},

/**
 * ドメイン別テーブルから板名を取得します。
 * @param {string} boardUrl 板URL
 * @return {string}
 */
getName(boardUrl){
	const parsed = this.parseBoardUrl(boardUrl);
	if(!parsed) return "(不明)";
	const { host, boardKey } = parsed;
	const domainGroup = this.getDomainGroup(host);
	const boardName = this.domainTables?.[domainGroup]?.[host]?.[boardKey];
	if(boardName) return boardName;
	return this.nameTbl[boardKey ? `${host}/${boardKey}/` : `${host}/`] || "(不明)";
},

/**
 * スレッドURLから板URLに変換します。
 * @param {string} url スレッドURL
 * @return {string} 板URL
 */
getUrlByThread(url){
	if(url.indexOf("test/read.cgi") !== -1){
		return url.replace(/^(https?:\/\/[^\/]+)\/test\/read\.cgi\/([^\/]+)\/.*/, "$1/$2/");
	}else if(url.indexOf("bbs/read.cgi") !== -1){
		if(url.indexOf("jbbs.") !== -1){
			return url.replace(/^(https?:\/\/[^\/]+)\/bbs\/read\.cgi\/([^\/]+)\/([^\/]+)\/.*/, "$1/$2/$3/");
		}else{
			return url.replace(/^(https?:\/\/[^\/]+)\/bbs\/read\.cgi\/([^\/]+)\/.*/, "$1/$2/");
		}
	}else{
		console.log("lego-ex-R:[ERROR]:Board:getUrlByThread(" + url + ") Not thread!");
		return url;
	}
}

